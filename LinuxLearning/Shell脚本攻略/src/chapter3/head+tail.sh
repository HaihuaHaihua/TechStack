# "head" is used to read the head lines of the file
head head_tail.txt
# option '-n {num}' can be used to set the number of lines to be read
head -n 4 head_tail.txt

# "tail" is used to read the end lines of the file
tail head_tail.txt
# option '-n {num}' can be used to set the number of lines to be read
tail -n 5 head_tail.txt
# command "tail" has an important usage that is to get the updating file's new contents


